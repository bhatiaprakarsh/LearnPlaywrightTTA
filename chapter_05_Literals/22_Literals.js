// JavaScript Literals

// A literal is a fixed value written directly in source code.
// Literals are the actual values, not variables or expressions.

// Numeric literals
const count = 10;
const price = 3.99;
const hexValue = 0xFF; // 255 in hexadecimal
const binaryValue = 0b1010; // 10 in binary

// String literals
const name = "Alice";
const greeting = 'Hello world';
const message = `Welcome, ${name}!`;

// Boolean literals
const isActive = true;
const isDone = false;

// Object literal                       
const user = {
  name: "Alice",
  age: 30,
  isMember: true
};

// Array literal
const scores = [100, 95, 87];

// Null literal
const emptyValue = null;

// Regular expression literal
const pattern = /hello/i;

// Notes:
// - Numeric literals include integers, decimals, hex, binary, and octal notations.
// - String literals can use single quotes, double quotes, or template literals with backticks.
// - Boolean literals are only true or false.
// - Object literals are written as {...} with key/value pairs.
// - Array literals are written as [...] with comma-separated values.
// - null is a literal representing "no value".
// - Regular expressions are also literals in slash notation.
