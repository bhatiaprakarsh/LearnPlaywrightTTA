// Increment and decrement operators change a value by 1.
// ++ increases by 1
// -- decreases by 1

let count = 5;
console.log(count++); // 5
console.log(count);   // 6

let number = 10;
console.log(++number); // 11
console.log(number);   // 11

let value = 8;
console.log(value--); // 8
console.log(value);   // 7

let x = 4;
console.log(--x); // 3
console.log(x);   // 3
