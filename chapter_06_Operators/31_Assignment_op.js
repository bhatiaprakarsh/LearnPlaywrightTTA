// Assignment operators are used to assign values to variables.
// They can also combine an operation with assignment.

let a = 10;
console.log("Initial a =", a); // 10

a += 5; // a = a + 5 //compound assignment operator
console.log("After += :", a); // 15

a -= 3; // a = a - 3
console.log("After -= :", a); // 12

a *= 2; // a = a * 2
console.log("After *= :", a); // 24

a /= 4; // a = a / 4
console.log("After /= :", a); // 6

a %= 4; // a = a % 4
console.log("After %= :", a); // 2

let name = "Java";
name += "Script";
console.log("String example:", name); // JavaScript
